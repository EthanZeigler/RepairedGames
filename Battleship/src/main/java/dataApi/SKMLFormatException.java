package dataApi;

public class SKMLFormatException extends Exception {
	private static final long serialVersionUID = 1L;

	public SKMLFormatException() {
		super();
	}

	public SKMLFormatException(String message) {
		super(message);
	}

	public SKMLFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public SKMLFormatException(Throwable cause) {
		super(cause);
	}
}
